// let
let namae = "Rifai";
console.log(namae); // Output: Rifai

// const
const age = 17;
console.log(age); // Output: 17

// arrow function
const greet = (name) => {
    console.log(`Hello, ${name}!`);
};
greet("Rifai"); // Output: Hello, Rifai!

// template literals
const boy = "Bowo";
const message = `Hey, ${boy}!`;
console.log(message); // Output: Hey, Bowo!